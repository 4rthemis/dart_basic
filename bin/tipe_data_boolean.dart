// Boolean

/**
 * Boolean adalah tipe data yang memiliki dua nilai, yaitu benar atau salah
 * Boolean direpresentasikan dengan kata kunci boool
 * untuk nilai benar, menggunakan kata kunci true
 * untuk nilai salah, menggunakan kata kunci false
 */ ///
void main() {
  bool age = true;

  if (age != true) {
    print('ternyata benar');
  } else {
    print('ternyata salah');
  }
}
