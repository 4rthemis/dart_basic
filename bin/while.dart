void main() {
  /** While Loop
   *  while loop adalah versi perulangan yang lebih sederhana dibanding for loop
   *  Di while loop hanya terdapat kondisi perulangan tanpa ada init statement dan post statement
   */

  var age = 12;

  if (age >= 10) {
    print('perulangan ke -$age');
    age++;
  }
}
