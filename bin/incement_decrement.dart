///Increment dan Decrement
///++var var = var + 1 (expression is var +1)
///var ++ var = var + 1 (expression is var)
///--var var = var -1 (expression is var -1)
///var-- var = var -1 (expression is var)
///

void main() {
  var i = 20;

  i++; // artinya 20 + 1 = 21

  print(i);

  var j = ++i; // artinya nilai i(21) + 1 = 22
  print(j);
}
